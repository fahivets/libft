#include "libft.h"

char	*ft_strdup(const char *src)
{
	char		*dup;
	int			len;
	int			i;

	len = 0;
	while (src[len])
		len++;
	dup = (char *)malloc(sizeof(*dup) * (len + 1));
	if (dup == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
